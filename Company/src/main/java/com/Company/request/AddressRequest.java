package com.Company.request;





public class AddressRequest {
	
	private Long id;
	

	private String city;
	

	private String state;
	

	private String pincode;
	
	
	

	public Long getId() {
		return id;
	}

	public String getCity() {
		return city;
	}

	public String getState() {
		return state;
	}

	public String getPincode() {
		return pincode;
	}

	
	
	

}
